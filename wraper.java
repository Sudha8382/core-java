public class wraper {
    public static void main(String[] args) {
        int a=10;
        Integer b=10;
        System.out.println(a);
        System.out.println(b);
        Float c=10.3f;
        System.out.println(c);
        boolean d=true;
        System.out.println(d);
        Integer e=a;
        System.out.println(e);
    }
    
}
